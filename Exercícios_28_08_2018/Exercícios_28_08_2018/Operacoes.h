#pragma once
double soma(double valor1, double valor2)
{
	return valor1 + valor2;
}

double subtracao(double valor1, double valor2)
{
	return valor1 - valor2;
}

double multiplicacao(double valor1, double valor2)
{
	return valor1 * valor2;
}

double divisao(double valor1, double valor2)
{
	return valor1 / valor2;
}
